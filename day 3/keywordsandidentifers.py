#Keywords are predefined, reserved words used in Python programming that have special meanings to the compiler.

# Example of using Python keywords


if True:  # 'if' and 'True' are keywords
    print("This is a Python keyword example.")  # 'print' is a built-in function
else:  # 'else' is also a keyword
    print("This won't run because the condition is always True.")



#Identifiers are the name given to variables, classes, methods(functions).

# Example of using Identifiers for variables
name = "John"  # 'name' is an identifier
age = 25       # 'age' is an identifier

# Output variables using identifiers
print("Name:", name)
print("Age:", age)